function App() {  
  const [soundType,setSoundType] = useState("heaterKit");
  const [sound,setSound] = useState(soundGroup[soundType]);
  const [display,setDisplay] = useState("");
 
  const soundName = {
    heaterKit: "Heater kit",
    smoothPianoKit: "Smooth piano kit"
  }

  const changeSoundGroup = () => { //Funcion para mostrar el grupo de sonido
    if(soundType === "heaterKit") {
      setSoundType("smoothPianoKit")
      setSound(soundGroup.smoothPianoKit)
      setDisplay("")
          } else {
      setSoundType("heaterKit")
      setSound(soundGroup.heaterKit)
      setDisplay("")
    }
  }
  return (
    <div className="App">
      
      <div className='drum-machine' id="drum-machine">
        <div className='keypadContainer'>
        <Keyboard sound={sound} setDisplay={setDisplay}/>
        </div>
        <div className='mixer'>
        <SoundChanger changeSoundGroup={changeSoundGroup}/>
        <Display display={display} soundType={soundType} soundName={soundName} />
        </div>
      </div>
    </div>
    
  );
}

export default App;

